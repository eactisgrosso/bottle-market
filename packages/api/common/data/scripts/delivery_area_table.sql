CREATE TABLE delivery_area (
  id uuid primary key,
  store_id uuid references store (id),
  name varchar(50),
  address varchar(255),
  geom geometry(POINT,4326),
  radius int,
  monday bool,
  tuesday bool,
  wednesday bool,
  thursday bool,
  friday bool,
  saturday bool,
  sunday bool,
  monday_hours_from time,
  monday_hours_to time,
  tuesday_hours_from time,
  tuesday_hours_to time,
  wednesday_hours_from time,
  wednesday_hours_to time,
  thursday_hours_from time,
  thursday_hours_to time,
  friday_hours_from time,
  friday_hours_to time,
  saturday_hours_from time,
  saturday_hours_to time,
  sunday_hours_from time,
  sunday_hours_to time,  
  date_added timestamp
);

CREATE INDEX idx_deliver_area_geom ON delivery_area USING gist(geom);  
