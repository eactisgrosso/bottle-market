CREATE TABLE "user" (
  id uuid primary key,
  authsub varchar(50) DEFAULT NULL,
  last_login timestamp DEFAULT NULL,
  email varchar(254) NOT NULL,
  firstname varchar(255) NOT NULL,
  lastname varchar(255) NOT NULL,
  company varchar(255),
  date_of_birth date,
  date_added timestamp NOT NULL,
  date_last_modified timestamp NOT NULL,
  genre smallint,
  nationality varchar(2),
  cuit varchar(13),
  phone varchar(50),
  phone_mobile varchar(50),
  zip varchar(15),
  city varchar(50),
  state varchar(50),
  country varchar(2),
  address varchar(255),
  is_admin bool NOT NULL DEFAULT false,
  comments text,
  _json json,
  type smallint,
  company_title varchar(255),
  slug varchar(255),
  logo varchar(100),
  site_url varchar(200),
  is_enabled bool DEFAULT true,
  user_parent_id uuid references "user" (id),
  company_position varchar(255),
  comments_user text,
  is_active bool DEFAULT true,
  media_companies varchar(255),
  media_format varchar(50),
  unique (email),
  unique (slug)
);

